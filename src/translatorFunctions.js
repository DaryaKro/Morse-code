const textToMorse = (text) => {
    return text.toLowerCase().split('').map(char => morseCodeDict[char] || char).join(' ');
};

const textToBinary = (text) => {
    return text.split('').map(char => {
        return char.charCodeAt(0).toString(2).padStart(8, '0');
    }).join(' ');
};

